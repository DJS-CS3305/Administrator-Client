    private void changeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeButtonActionPerformed
        //change the maintenance time to the user's selection
        String dateString = dayField.getText() + "/" + monthField.getText() + "/" +
                yearField.getText() + "/" + hoursField.getText() + "/" +
                minutesField.getText();
        
        try {
            Date date = FORMAT.parse(dateString);
            MaintenanceMessage msg = new MaintenanceMessage(Connector.getNextId(),
                    date.getTime());
            Connector.getSocket().sendMessage(msg);
            feedback.setText("Time set.");
        }
        catch(Exception e) {
            ErrorLogger.get().log(e.toString());
            e.printStackTrace();
            feedback.setText("There has been an error in setting the maintenance time.");
        }
    }//GEN-LAST:event_changeButtonActionPerformed

