    public MaintenancePane() {
        initComponents();
        
        //get time from server
        MaintenanceMessage msg = new MaintenanceMessage(Connector.getNextId());
        Connector.getSocket().sendMessage(msg);
        MaintenanceMessage reply = (MaintenanceMessage) 
                Connector.getSocket().receiveMessage();
        long millis = (Long) reply.getContent().get(MaintenanceMessage.TIME);
        Date date = new Date(millis);
        
        //put values into fields
        try {
            String dateString = FORMAT.format(date);
            String[] values = dateString.split("/");
            dayField.setText(values[0]);
            monthField.setText(values[1]);
            yearField.setText(values[2]);
            hoursField.setText(values[3]);
            minutesField.setText(values[4]);
        }
        catch(Exception e) {
            ErrorLogger.get().log(e.toString());
            e.printStackTrace();
        }
    }

